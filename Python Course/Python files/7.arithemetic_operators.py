"""
+   addition
-   subtraction
*   multiplication
/   division
//  floor division (division without decial values)
**  exponent (power values [eg: square])
%   modulo (remainder)
"""

a=int(input("Enter num 1 : "))
b=int(input("Enter num 2 : "))
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a**b)
print(a%b)
